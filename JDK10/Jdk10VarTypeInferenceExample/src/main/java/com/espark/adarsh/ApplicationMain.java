package com.espark.adarsh;

public class ApplicationMain {


    public static void main(String[] args) {

        var myCharacter= 'A';
        System.out.println("Character "+myCharacter);

        var myString = "this is my name";
        System.out.println("String "+myString);

        var myInteger = 1000;
        System.out.println("Integer "+myInteger);

        var myLong = 10000000L;
        System.out.println("Long "+myLong);

        var myFloat = 10.3F;
        System.out.println("Float "+myFloat);

        var myDouble = 10.3D;
        System.out.println("Double "+myDouble);

        var myBoolean = false;
        System.out.println("Boolean "+myBoolean);

        var myUnicodeCharacter= '\uface';
        System.out.println("UniCode Character "+myUnicodeCharacter);

        var myOctalNumber=0767;
        System.out.println("Octal Integer "+myOctalNumber);

        var myHexaNumber=0X76A;
        System.out.println("Hexa Decimal Integer "+myHexaNumber);



    }
}
